// production error handler
// no stacktraces leaked to user
//
// NOTE using this as the production and dev error handler.
router.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
        message: err.message,
        error: err
    });
});

module.exports = router;
